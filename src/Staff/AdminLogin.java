    //Validate Username & Password
    private boolean ValidateAccount() {
        boolean isAuthenticated = false;
        saveDir = System.getProperty("user.dir") + "\\src\\DB_txtfiles\\"; //Directory to Admin.txt
        try {
            String tempoUser = tfUsername.getText(); //Assign Variable to store username input.
            String tempoPass = String.valueOf(pfPassword.getPassword()); //Assign Variable to store password input.

            File adminAccTxt = new File(saveDir + "Admin.txt");

            if (!adminAccTxt.exists()) {
                adminAccTxt.createNewFile();
            }

            Scanner inputFile = new Scanner(adminAccTxt);
            String[] matchedID;
            while (inputFile.hasNext()) {
                String entry = inputFile.nextLine();
                matchedID = entry.split(":");
                if (tempoUser.equals(matchedID[1]) && tempoPass.equals(matchedID[2])) { //Check if User Inputs match Database.
                    isAuthenticated = true;
                    AdminID = matchedID[0];
                    username = matchedID[1];
                }
            }
            inputFile.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        return isAuthenticated;
    }

    //Create Session
    private void startSession() { //Create Cache File storing Admin's Username and ID.
        try {
            saveDir = System.getProperty("user.dir") + "\\src\\DB_txtfiles\\";
            File cache = new File(saveDir + "Cache.txt");
            if (!cache.exists()) {
                cache.createNewFile();
            }
            FileWriter fw = new FileWriter(saveDir + "Cache.txt", true);
            PrintWriter pw = new PrintWriter(fw);
            pw.println(AdminID + ":" + username);
            pw.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

