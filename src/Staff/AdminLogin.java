    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if ("".equals(tfUsername.getText()) || "".equals(String.valueOf(pfPassword.getPassword()))) {
            HighlightFields();
            JOptionPane.showMessageDialog(null, "Empty Fields Detected! Please Fill Them Up!", "Empty Fields", JOptionPane.WARNING_MESSAGE);
            try {
                LoginEmptyField();
            } catch (Exception ex) {
                Logger.getLogger(AdminLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if ("*".equals(tfUsername.getText()) || "*".equals(String.valueOf(pfPassword.getPassword()))) {
            JOptionPane.showMessageDialog(null, "Invalid Username & Passsword Provided!", "Invalid Username & Password", JOptionPane.WARNING_MESSAGE);
        } else if (!ValidateAccount()) {
            JOptionPane.showMessageDialog(null, "Username or Password Provided is False!", "Login Failure", JOptionPane.WARNING_MESSAGE);
        } else if (ValidateAccount()) {
            DeHighlightFields();
            startSession();
            JOptionPane.showMessageDialog(null, "Welcome Back " + username, "Successful Login", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            AdminMainMenu amm;
            try {
                amm = new AdminMainMenu();
                amm.setVisible(true);
            } catch (IOException ex) {
                Logger.getLogger(AdminLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnLoginActionPerformed

